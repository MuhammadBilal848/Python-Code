def add(num1,num2):
    return num1+num2

def sub(num1,num2):
    return num1-num2

def mult(num1,num2):
    return num1*num2

def floor_div(num1,num2):
    if num2==0:
        return "Sorry you have entered zero as denominator"
    return num1//num2

def float_div(num1,num2):
    if num2==0:
        return "Sorry you have entered zero as denominator"
    return num1/num2