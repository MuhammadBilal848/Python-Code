print(3**3) # '**' it gives square
print(9**0.5) # "0.5" gives square root
print(3**0.5)
print(round(3**0.5,5)) #round reduces decimal numbers
print(4%2) # where "%" is called modulo which gives remainder
print(2**3**2) # here precedence of exponents works from right to left i.e. 2**3**2=2**9=512
# Precedence Rule   
# parenthesis Highest 
# exponents Right to Left
# *,/,//,% Left to right
# +,- Left to right
